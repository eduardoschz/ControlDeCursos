    private void txtNdCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNdCKeyTyped
        char t = evt.getKeyChar();
        if (!Character.isDigit(t) && t != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
            getToolkit().beep();
        } else {
            txtNdCKeyPressed(evt);
        }
    }//GEN-LAST:event_txtNdCKeyTyped

    private void caracteres(KeyEvent chars) {

        char t = chars.getKeyChar();
        if (!Character.isDigit(t) && t != KeyEvent.VK_BACK_SPACE) {
            chars.consume();
            getToolkit().beep();
        }
    }

    @Override
    public void run() {
        Thread ct = Thread.currentThread();
        while (ct == hilo1) {
            calcula();
            lblTime.setText(hora + ":" + minutos + ":" + segundos + " ");
            try {
                Thread.sleep(1000);
            } catch (InterruptedException e) {
            }
        }
    }

    public void calcula() {
        Calendar tiempoCal = new GregorianCalendar();
        Date fechaHoraActual = new Date();

        tiempoCal.setTime(fechaHoraActual);

        hora = String.valueOf(tiempoCal.get(Calendar.HOUR_OF_DAY));
        minutos = String.valueOf(tiempoCal.get(Calendar.MINUTE));
        segundos = String.valueOf(tiempoCal.get(Calendar.SECOND));
        //time = hora + ":" + minutos + ":" + segundos;
    }

    private void numero(String n) {
        this.txtNdC.setText(this.txtNdC.getText() + n);
    }

    private void buscar() {
        if (this.txtNdC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo no puede estar vacio", "CAE - Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (archivo.buscarNumeroDeCuenta(this.txtNdC.getText())) {
            this.lblSetNombre.setText("ERR");
            this.lblSetApellidos1.setText("ERR");
            this.lblSetApellidos2.setText("ERR");
            this.lblSetNoDeC.setText("ERR");
        }else{
            this.txtNdC.setText("");
            temp = archivo.getBuscado();
            
            this.lblSetNombre.setText(temp.getNombre());
            this.lblSetApellidos1.setText(temp.getApellidoPaterno());
            this.lblSetApellidos2.setText(temp.getApellidoMaterno());
            this.lblSetNoDeC.setText(temp.getNumeroDeCuenta());
        }
    }

